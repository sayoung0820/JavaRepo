public class Notes {
    public static void main(String[] args) {

    /* Operators = symbols to perform operations on variables and values. They include
    arithmetic, assignment, relational, and logical.
    Arithmetic = +. -. *, /, % (finds the remainder)
    Relational = compare values and return true or false
    Logical = boolean expressions
    Assignment = assign values to variables

    Assignment operator:
    = - simple assignment for value
    += - Add And, adds the right value to the left and reassigns the result
    -= - Subtract and, subtracts right value from left and reassigns the result
    *= - Multiply and, multiplies the right value with the left and reassigns value
    /= - Divide and, divides the variable on the left by the value on the right and reassigns
    ++ - increased value of variable by 1
    -- = subtract 1 from value of variable

    Comparison Operators:
    == - Equal to
    != - Not equal to
    >= - Greater than or equal to, checks if left value is greater or equal to right

    Logical Operators:
    logical and &&, Logical OR ||, and logical NOT !
    Help evaluate expressions and produce boolean results
     */

    System.out.println("Arithmetic Operators");
    System.out.println("Resetting x to 0...");
    System.out.println("x = 0");
    int x = 0;
    System.out.print("The value of x is now: ");
    System.out.println(x);

    System.out.println("x = x + 5");
    x = x + 5;
    System.out.println("The value of x is now: ");
    System.out.println(x);

    System.out.println("x + = 5");
    x += 5;
    System.out.println("The value of x is now: ");
    System.out.println(x);

    System.out.println("x = x -2");
    x = x -2;
    System.out.println("The value of x is now: ");
    System.out.println(x);

    System.out.println("x = x * 4");
    x = x * 4;
    System.out.println("The value of x is now: ");
    System.out.println(x);

    System.out.println("x = x / 3");
    x = x / 3;
    System.out.println("The value of x is now: ");
    System.out.println(x);

    System.out.println("Truncation");
    System.out.println("int thisTruncates = 10 / 3");
    int thisTruncates = 10 / 3;
    System.out.println("thisTruncates is now:");
    System.out.println(thisTruncates);

    System.out.println("The Modulus Operator");
    System.out.println("int wholeNumber = 10 / 3");
    int wholeNumber = 10 /3;
    System.out.println("int remainder = 10 % 3;");
    int remainder = 10 % 3;
    System.out.println("wholeNumber is now:");
    System.out.println(wholeNumber);
    System.out.println("remainder is now:");
    System.out.println(remainder);










    }
}
